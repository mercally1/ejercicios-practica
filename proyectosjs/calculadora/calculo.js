let firstnumber = parseInt(prompt("Ingrese un numero cualquiera"))
let secondnumber = parseInt(prompt("Ingrese un numero cualquiera"))

suma = firstnumber +  secondnumber 
resta = firstnumber- secondnumber
multiplica = firstnumber * secondnumber
divide = firstnumber / secondnumber

document.writeln('El resultado de la Suma es: ' + suma + '<br>')
document.writeln('El resultado de la Resta:' + resta + '<br>')
document.writeln('El resultado de la Multiplicación es: ' + multiplica + '<br>')
document.writeln('El resultado de la División es: ' + divide + '<br>')